package com.example.openapp;

public class Complaint {
    public String name;
    public String email;
    public String Phone;
    public String adhar;
    public String Complaint;

    public Complaint( String name, String email, String adhar, String phone, String complaint ) {
        this.name = name;
        this.email = email;
        this.adhar = adhar;
        Phone = phone;
        Complaint = complaint;
    }
}
